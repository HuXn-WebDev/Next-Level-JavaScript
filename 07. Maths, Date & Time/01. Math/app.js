// The JavaScript Math object allows you to perform mathematical tasks on numbers.

Math.PI;
Math.E; // returns Euler's number
Math.PI; // returns PI
Math.SQRT2; // returns the square root of 2
Math.SQRT1_2; // returns the square root of 1/2
Math.LN2; // returns the natural logarithm of 2
Math.LN10; // returns the natural logarithm of 10
Math.LOG2E; // returns base 2 logarithm of E
Math.LOG10E; // returns base 10 logarithm of E

// Math Methods -> Number to Integer
Math.round(4.6); // Returns x rounded to its nearest integer
Math.ceil(4.6); // Returns x rounded up to its nearest integer
Math.floor(4.6); // Returns x rounded down to its nearest integer
Math.trunc(4.6); // Returns the integer part of x (new in ES6)
Math.pow(8, 2); // returns the value of x to the power of y:
Math.abs(-4.7); // returns the absolute (positive) value of x:
Math.random(); // returns a random number between 0 (inclusive), and 1 (exclusive):

